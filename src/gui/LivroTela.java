    private void limparCampos(){
    isbnTXT.setText("");
    nomeTXT.setText("");
    autorTXT.setText("");
    editoraTXT.setText("");
    assuntoTXT.setText("");
    }
    
    private void CarregarCampos() {
         int setar = tabelaLivro.getSelectedRow();
         isbnTXT.setText(tabelaLivro.getModel().getValueAt(setar, 0).toString());
         nomeTXT.setText(tabelaLivro.getModel().getValueAt(setar, 1).toString());
         autorTXT.setText(tabelaLivro.getModel().getValueAt(setar, 2).toString());
         assuntoTXT.setText(tabelaLivro.getModel().getValueAt(setar, 3).toString());
         editoraTXT.setText(tabelaLivro.getModel().getValueAt(setar, 4).toString());
     }
    
    private void AlterarCampos () {
        try {
             Livro objLivro = new Livro();
        objLivro.setISBN(Integer.parseInt(isbnTXT.getText()));
        objLivro.setNomeLivro(nomeTXT.getText());
        objLivro.setAutor(autorTXT.getText());
        objLivro.setAssunto(assuntoTXT.getText());
        objLivro.setEditora_codigo(new Editora (Integer.parseInt(editoraTXT.getText())));
            // instanciando a classe UsuarioDAO do pacote dao e criando seu objeto dao
           objLivroDAO.alterar(objLivro);
        } catch (SQLException e) {
            JOptionPane.showConfirmDialog(null,"Erro ao Alterar\n"+e);
        }
       
    }
    private void ExcluirDados (){
        try {
                Livro objLivro = new Livro();
                objLivro.setISBN(Integer.parseInt(isbnTXT.getText()));
    
                objLivroDAO.deletar(objLivro);
        } catch (SQLException e) {
            JOptionPane.showConfirmDialog(null,"Erro ao Deletar\n"+e);
        }
       
        
    }
    private void salvar(){
        Livro objLivro = new Livro();
        objLivro.setISBN(Integer.parseInt(isbnTXT.getText()));
        objLivro.setEditora_codigo(new Editora (Integer.parseInt(editoraTXT.getText())));
        objLivro.setNomeLivro(nomeTXT.getText());
        objLivro.setAutor(autorTXT.getText());
        objLivro.setAssunto(assuntoTXT.getText());
        objLivro.setQuantidade(Integer.parseInt(quantidadeTXT.getText()));
        
        LivroDAO livroDAO = new LivroDAO();
        livroDAO.salvarLivro(objLivro);
    }
