     private void listarDados (){
        try {
            DefaultTableModel model = (DefaultTableModel)tabelaLivro.getModel();
            model.setNumRows(0);
            
            List<Livro> livroLista = objLivroDAO.ListarDados();
            for (int i = 0; i < livroLista.size(); i++) {
                model.addRow(new Object[]{
                   livroLista.get(i).getISBN(),
                   livroLista.get(i).getNomeLivro(),
                   livroLista.get(i).getAutor(),
                   livroLista.get(i).getAssunto(),
                   livroLista.get(i).getEditora_codigo().getNome(),
                   livroLista.get(i).getQuantidade()
                });
                  
            }
        } catch (SQLException erro) {
            JOptionPane.showConfirmDialog(null, "Erro ao listar"+erro);
        }
    }
