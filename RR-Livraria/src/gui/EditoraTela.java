     private void listarDados (){
        try {
           EditoraDAO objEditoraDAO = new EditoraDAO();
            DefaultTableModel model = (DefaultTableModel)tabelaEditoras.getModel();
            model.setNumRows(0);
            
            List<Editora> editoraLista = objEditoraDAO.listarTodos();
            for (int i = 0; i < editoraLista.size(); i++) {
                model.addRow(new Object[]{
                   editoraLista.get(i).getCodigo(),
                   editoraLista.get(i).getNome(),
                   editoraLista.get(i).getTelefone(),
                   editoraLista.get(i).getNome_gerente(),
                   editoraLista.get(i).getEndereco()
                });
                  
            }
        } catch (SQLException erro) {
            JOptionPane.showConfirmDialog(null, "Erro ao listar\n"+erro);
        }
    }
