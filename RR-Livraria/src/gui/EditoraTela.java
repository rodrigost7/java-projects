    private void CarregarCampos() {
         int setar = tabelaEditoras.getSelectedRow();
         codigoTXT.setText(tabelaEditoras.getModel().getValueAt(setar, 0).toString());
         nomeTXT.setText(tabelaEditoras.getModel().getValueAt(setar, 1).toString());
         telefoneTXT.setText(tabelaEditoras.getModel().getValueAt(setar, 2).toString());
         gerenteTXT.setText(tabelaEditoras.getModel().getValueAt(setar, 3).toString());
         enderecoTXT.setText(tabelaEditoras.getModel().getValueAt(setar, 4).toString());   
     }
    
     private void ExcluirDados (){
        int id;
        
         try {
             id = Integer.parseInt(codigoTXT.getText());
            Editora objEditora = new Editora();
            objEditora.setCodigo(id);

            EditoraDAO objEditoraDAO = new EditoraDAO();
            objEditoraDAO.deletar(objEditora);
         } catch (SQLException e) {
             JOptionPane.showConfirmDialog(null, "Erro ao deletar\n"+e);
         }
    }
     
     private void alterarCampos() {
         int id;
         id = Integer.parseInt(codigoTXT.getText());
         Editora objEditora = new Editora();
         objEditora.setCodigo(id);
         objEditora.setNome(nomeTXT.getText());
         objEditora.setTelefone(telefoneTXT.getText());
         objEditora.setNome_gerente(gerenteTXT.getText());
         objEditora.setEndereco(enderecoTXT.getText());
         EditoraDAO objEditoraDAO = new EditoraDAO();
         objEditoraDAO.alterar(objEditora);   
     }
     private void limparCampos(){
         codigoTXT.setText("");
         nomeTXT.setText("");
         enderecoTXT.setText("");
         gerenteTXT.setText("");
         telefoneTXT.setText("");
     }
     private void salvar(){
         Editora objEditora = new Editora();
        objEditora.setNome(nomeTXT.getText());
        objEditora.setEndereco(enderecoTXT.getText());
        objEditora.setNome_gerente(gerenteTXT.getText());
        objEditora.setTelefone(telefoneTXT.getText());
        
        EditoraDAO objEditoraDAO = new EditoraDAO();
        objEditoraDAO.salvarEditora(objEditora);
     }
